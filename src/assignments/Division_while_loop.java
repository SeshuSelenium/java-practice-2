package assignments;

public class Division_while_loop {

	public static void main(String[] args) { // main method
		int x=0; // variable x
		while(x<=100) { 	//while loop for looping 100 cycles
			x++;	// increment x value
			if(x%5==0) { // conditional statement
				System.out.println("100 divisible by 5 followd by that number is: "+ x); // print divisible values
			}
		}
		
		

	}

}
